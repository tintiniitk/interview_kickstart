modulo_base = 1e9 + 7


class Solution:
    def sumSubseqWidths(self, nums: list[int]) -> int:
        # print(f"Original array = {nums}")
        # sort nums - sorting this list doesn't change the answer, so sorting is okay.
        nums.sort()
        # print(f"Sorted array = {nums}")
        # if nums size is less than 2, return 0
        n = len(nums)
        print(f"n = {n}")
        if n < 2:
            return 0
        # total number of non-empty sub-sequences = 2^n - 1
        # total number of sub-sequences of size 1 = n => they all have max - min = 0, so they can be ignored.
        # For each 0<= i < j < n,
        #     for all sub-sequences which have max = nums[j] and min = nums[i],
        #         max - min = nums[j] - nums[i]
        #         count of such sub-sequences = 2 ^ (j-i-1)
        # So, we just need to sum up this remainder for every i,j combination
        cur = 0
        powers_of_two_modulo_base = dict()
        powers_of_two_modulo_base[0] = 1
        for i in range(1, n):
            powers_of_two_modulo_base[i] = int(
                (powers_of_two_modulo_base[i - 1] << 1) % modulo_base
            )
        # print(f"powers_of_two_modulo_base = {powers_of_two_modulo_base}")

        # Original approach based on all i,j pairs
        # for i in range(0, n - 1):
        #     for j in range(i+1, n):
        #         count = powers_of_two_modulo_base[j - i - 1]
        #         diff = nums[j] - nums[i]
        #         product = int((count * diff) % modulo_base)
        #         cur = cur + product
        #         cur = cur % modulo_base

        # Optimal approach based on fused i pairs
        for i in range(0, n):
            diff_of_power = int(
                (powers_of_two_modulo_base[i] - powers_of_two_modulo_base[n - 1 - i])
                % modulo_base
            )
            product = int((diff_of_power * nums[i]) % modulo_base)
            cur = cur + product
            cur = int(cur % modulo_base)

        return int(cur % modulo_base)


